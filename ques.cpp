//wap to take two interger as input and print their sum
#include <iostream>
using namespace std;

int main() {
    int num1, num2, sum;

    // Taking input
    cout << "Enter two integers: ";
    cin >> num1 >> num2;

    // Calculating sum
    sum = num1 + num2;

    // Printing the result
    cout << "Sum = " << sum << endl;

    return 0;
}

